# Ejemplos de operadores aritméticos

# Definimos dos variables
a = 10
b = 3

# Suma
resultado_suma = a + b
print(f"Suma: {a} + {b} = {resultado_suma}")

# Resta
resultado_resta = a - b
print(f"Resta: {a} - {b} = {resultado_resta}")

# Multiplicación
resultado_multiplicacion = a * b
print(f"Multiplicación = {a} * {b} = {resultado_multiplicacion}")

# División
resultado_division = a / b
print(f"División: {a} / {b} = {resultado_division}")

# División entera (devuelve la parte entera del cociente)
resultado_division_entera = a//b
print(f"División entera: {a} // {b} = {resultado_division_entera}")

# Módulo 
resultado_modulo = a % b
print(f"Modulo: {a} % {b} = {resultado_modulo}")

# Exponente
resultado_exponente = a ** b
print(f"Exponente: {a} ** {b} = {resultado_exponente}")


